int	ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	while (nb > i && nb <= 2147395600)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
